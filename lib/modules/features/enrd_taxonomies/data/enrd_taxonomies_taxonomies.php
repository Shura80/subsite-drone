/**
 * Helper function to create ENRD Taxonomies and import terms on install.
 *
 * @param string $machine_name
 *   The machine name of the vocabulary to create.
 *
 * @return mixed
 *   Return terms structure for the vocabulary.
 */
function _enrd_taxonomies_taxonomies($machine_name) {
  $list_function = "_enrd_taxonomies_$machine_name";
  if (function_exists($list_function)) {
    return $list_function();
  }
  else {
    return array();
  }
}
